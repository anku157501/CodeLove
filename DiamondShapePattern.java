import java.util.*;
class Solution {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt(),m=1,c=n/2;
        int l=0;
        for (int i = 0; i <=n/2 ; i++) {
            l=0;
            for (int j = 0; j <n; j++) {
                if (j>=c) {
                    //System.out.print(j);
                    System.out.print("*");
                l++;
                if(l==m)
                break;
                }
                else{
                    System.out.print(" ");
                }
                }
                m=m+2;c--;
                System.out.println();
                }
                c=1;m=m-4;l=0;
                for (int i = 0; i <n/2 ; i++) {
                 l=0;
            for (int j = 0; j <n; j++) {
                if (j>=c) {
                    //System.out.print(j);
                    System.out.print("*");
                l++;
                if(l==m)
                break;
                }
                else{
                    System.out.print(" ");
                }
                }
                m=m-2;c++;
                System.out.println();
                }
    }
}


